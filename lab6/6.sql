SELECT DISTINCT
	a.SUBJECT_T,
	(
		SELECT COUNT(*)
		FROM PROGRESS b
		WHERE a.SUBJECT_T = b.SUBJECT_T
			AND b.NOTE IN (8, 9)
	) [Count]
FROM PROGRESS a
GROUP BY a.SUBJECT_T, a.NOTE
HAVING a.NOTE IN (8, 9)
ORDER BY [Count] DESC;