SELECT min(AUDITORIUM_CAPACITY)            [Минимальная],
	   max(AUDITORIUM_CAPACITY)            [Максимальная],
	   AVG(AUDITORIUM_CAPACITY)            [Средняя],
	   count(*)                            [Кол-во аудиторий],
	   sum(AUDITORIUM_CAPACITY)            [Сумма]
From AUDITORIUM  

-- Задание 2

SELECT AUDITORIUM_TYPE.AUDITORIUM_TYPENAME,
	min(AUDITORIUM.AUDITORIUM_CAPACITY)            [Минимальная],
	   max(AUDITORIUM.AUDITORIUM_CAPACITY)         [Максимальная],
	   AVG(AUDITORIUM.AUDITORIUM_CAPACITY)         [Средняя],
	   count(*)                                    [Кол-во аудиторий],
	   sum(AUDITORIUM.AUDITORIUM_CAPACITY)         [Сумма]
From AUDITORIUM 
JOIN AUDITORIUM_TYPE ON AUDITORIUM.AUDITORIUM_TYPE = AUDITORIUM_TYPE.AUDITORIUM_TYPE
GROUP BY AUDITORIUM_TYPE.AUDITORIUM_TYPENAME;

-- Задание 3

Select * 
From 
(SELECT
CASE 
		    when (PROGRESS.NOTE = 10) then '10'
			when (PROGRESS.NOTE IN (8, 9)) then '8-9'
			when (PROGRESS.NOTE IN (6, 7)) then '6-7'
			when (PROGRESS.NOTE IN (4, 5)) then '4-5'
END [Notes],
COUNT(*) [Count]
	FROM PROGRESS
	GROUP BY
		CASE
			when (PROGRESS.NOTE = 10) then '10'
			when (PROGRESS.NOTE IN (8, 9)) then '8-9'
			when (PROGRESS.NOTE IN (6, 7)) then '6-7'
			when (PROGRESS.NOTE IN (4, 5)) then '4-5'
		END
) AS T
ORDER BY
	CASE [Notes]
		when '10'  then 1
		when '8-9' then 2
		when '6-7' then 3
		when '4-5' then 4
		else 0
	END;

